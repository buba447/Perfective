#import "UIColor+Perfective.h"

@implementation UIColor (Perfective)

+ (UIColor *)perfectiveDarkBackgroundColor {
  return [UIColor colorWithRed:0.18 green:0.18 blue:0.18 alpha:1];
}

+ (UIColor *)perfectiveSelectionColor {
  return [UIColor colorWithRed:0.42 green:0.52 blue:0.66 alpha:1];
}

+ (UIColor *)perfectiveActiveColor {
  return [UIColor colorWithRed:0.91 green:0.43 blue:0.55 alpha:1];
}

@end
